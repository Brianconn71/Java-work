package Inheritance;

public class Driver {
	private String name;
	private int phoneNumber;
	
	public Driver() {
		this.name = name;
		this.phoneNumber = phoneNumber;
	}
	
	public Driver(String name, int phoneNumber) {
		this.name = name;
		this.phoneNumber = phoneNumber;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String aName) {
		this.name = aName;
	}
	
	public int getPhoneNumber() {
		return phoneNumber;
	}
	
	public void setPhoneNumber(int aNumber) {
		this.phoneNumber = aNumber;
	}
	

}
